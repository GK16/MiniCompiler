package guikai.P1;

public class Sym {
    // type
    private String type;

    // initialize the Sym to have the given type.
    public Sym(String type){
        this.type = type;
    }

    // Return this Sym's type.
    public String getType(){
        return type;
    }

    // Return this Sym's type.
    public String toString(){
        return type;
    }

}
